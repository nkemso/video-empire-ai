export function getStrategyPrompt(
  mode: number,
  topic: string,
  context: {
    audience?: string;
    platform?: string;
    tone?: string;
  } = {}
): string {
  const modePrompt = MODE_PROMPTS[mode as keyof typeof MODE_PROMPTS];
  
  if (!modePrompt) {
    throw new Error(`Invalid strategy mode: ${mode}`);
  }

  const contextSection = `
USER INPUT:
- Topic/Niche: ${topic}
${context.audience ? `- Target Audience: ${context.audience}` : ''}
${context.platform ? `- Primary Platform: ${context.platform}` : ''}
${context.tone ? `- Brand Tone: ${context.tone}` : ''}

Now generate the complete strategy:
`;

  return `${SYSTEM_PROMPT}\n\n${modePrompt}\n\n${contextSection}`;
  }
